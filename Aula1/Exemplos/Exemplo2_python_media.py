print("Olá, usuário! Vou fazer a média para você de três salários: ")
salario1 = float(input("Digite o primeiro salário: "))
salario2 = float(input("Digite o segundo salário: "))
salario3 = float(input("Digite o terceiro salário: "))

media_de_salarios = (salario1 + salario2 + salario3) / 3

print("O resultado da média é: ", media_de_salarios)
